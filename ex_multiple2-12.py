# โปรแกรมแม่สูตรคูณ 2-12 (ใช้ while และ if)

print("โปรแกรมแม่สูตรคูณ 2 ถึง 12\n" + "="*30)

# ตัวแปรสำหรับควบคุมแม่สูตรคูณ
แม่ = 2

while แม่ <= 12:
    print(f"\nแม่ {แม่}")
    print("-"*20)
    
    # ตัวแปรสำหรับตัวคูณ (1-12)
    ตัวคูณ = 1
    
    while ตัวคูณ <= 12:
        ผลคูณ = แม่ * ตัวคูณ
        
        # ใช้ if เพื่อจัดรูปแบบให้สวยงาม (ตัวเลขหลักเดียวกับสองหลัก)
        if ผลคูณ < 10:
            print(f"   {แม่} x {ตัวคูณ} =  {ผลคูณ}")
        else:
            print(f"   {แม่} x {ตัวคูณ} = {ผลคูณ}")
            
        ตัวคูณ += 1
    
    # เพิ่มไปยังแม่ถัดไป
    แม่ += 1

print("\n" + "="*30)
print("จบการแสดงแม่สูตรคูณทั้งหมด")