# ใช้เป็นข้อสอบ ข้อ 2 10 คะแนน 2-68 IT67-01
แม่ = 2

while แม่ <= 12:
    ตัวคูณ = 1
    print(f"\nแม่ {แม่}:")
    
    while ตัวคูณ <= 12:
        ผล = แม่ * ตัวคูณ
        # ใช้ if ช่วยจัดช่องว่างให้เรียงสวย
        if ผล < 10:
            print(f"{แม่} × {ตัวคูณ:<2} =  {ผล}")
        else:
            print(f"{แม่} × {ตัวคูณ:<2} = {ผล}")
        ตัวคูณ += 1
        
    แม่ += 1